/**
 * Утилиты для работы с датами
 */
export const DateUtils = {
    /**
     * Преобразование года в число для сравнения
     * @param {string} yearStr - Год в формате "число до н.э." или "число н.э." или "век"
     * @returns {number} - Числовое значение года (отрицательное для до н.э.)
     */
    yearToNumber: (yearStr) => {
        if (!yearStr) return 0;
        
        // Обработка "до н.э."
        if (yearStr.includes('до н.э.')) {
            const num = parseInt(yearStr.replace(/[^0-9]/g, ''));
            return -num;
        }
        
        // Обработка "н.э." или без указания
        if (yearStr.includes('н.э.')) {
            const num = parseInt(yearStr.replace(/[^0-9]/g, ''));
            return num;
        }
        
        // Обработка веков
        if (yearStr.includes('век')) {
            const num = parseInt(yearStr.replace(/[^0-9]/g, ''));
            if (yearStr.includes('до н.э.')) {
                return -(num * 100);
            }
            return num * 100;
        }
        
        // Просто число
        const num = parseInt(yearStr.replace(/[^0-9-]/g, ''));
        return isNaN(num) ? 0 : num;
    },
    
    /**
     * Сортировка массива дат по году
     * @param {Array} datesArray - Массив дат
     * @returns {Array} - Отсортированный массив
     */
    sortByYear: (datesArray) => {
        return [...datesArray].sort((a, b) => {
            return DateUtils.yearToNumber(a.year) - DateUtils.yearToNumber(b.year);
        });
    },
    
    /**
     * Получение уникальных периодов из массива дат
     * @param {Array} datesArray - Массив дат
     * @returns {Array} - Уникальные периоды
     */
    getUniquePeriods: (datesArray) => {
        const periods = new Set();
        datesArray.forEach(d => {
            if (d.period) periods.add(d.period);
        });
        return Array.from(periods).sort();
    },
    
    /**
     * Получение уникальных регионов из массива дат
     * @param {Array} datesArray - Массив дат
     * @returns {Array} - Уникальные регионы (плоский массив)
     */
    getUniqueRegions: (datesArray) => {
        const regions = new Set();
        datesArray.forEach(d => {
            if (d.region && Array.isArray(d.region)) {
                d.region.forEach(r => regions.add(r));
            }
        });
        return Array.from(regions).sort();
    },
    
    /**
     * Получение уникальных категорий из массива дат
     * @param {Array} datesArray - Массив дат
     * @returns {Array} - Уникальные категории (плоский массив)
     */
    getUniqueCategories: (datesArray) => {
        const categories = new Set();
        datesArray.forEach(d => {
            if (d.category && Array.isArray(d.category)) {
                d.category.forEach(c => categories.add(c));
            }
        });
        return Array.from(categories).sort();
    },
    
    /**
     * Получение облака тегов с частотами
     * @param {Array} datesArray - Массив дат
     * @returns {Object} - Объект с тегами и частотой их использования
     */
    getTagsCloud: (datesArray) => {
        const tags = {};
        datesArray.forEach(d => {
            if (d.tags && Array.isArray(d.tags)) {
                d.tags.forEach(tag => {
                    tags[tag] = (tags[tag] || 0) + 1;
                });
            }
        });
        return tags;
    },
    
    /**
     * Фильтрация дат по множественным критериям
     * @param {Array} datesArray - Массив дат
     * @param {Object} filters - Объект с фильтрами
     * @returns {Array} - Отфильтрованный массив
     */
 /**
     * Фильтрация дат по множественным критериям
     * @param {Array} datesArray - Массив дат
     * @param {Object} filters - Объект с фильтрами
     * @returns {Array} - Отфильтрованный массив
     */
    filterDates: (datesArray, filters) => {
        // Если filters не передан или undefined, создаем пустой объект
        if (!filters) {
            filters = {};
        }
        
        return datesArray.filter(date => {
            // Поиск по тексту - проверяем что filters.search существует и не пустой
            if (filters.search && typeof filters.search === 'string' && filters.search.trim() !== '') {
                const searchText = filters.search.toLowerCase();
                const matches = 
                    (date.event && date.event.toLowerCase().includes(searchText)) ||
                    (date.description && date.description.toLowerCase().includes(searchText)) ||
                    (date.fullDescription && date.fullDescription.toLowerCase().includes(searchText)) ||
                    (date.tags && Array.isArray(date.tags) && date.tags.some(t => t && t.toLowerCase().includes(searchText)));
                
                if (!matches) return false;
            }
            
            // Фильтр по периодам (множественный выбор)
            if (filters.periods && Array.isArray(filters.periods) && filters.periods.length > 0) {
                if (!date.period || !filters.periods.includes(date.period)) return false;
            }
            
            // Фильтр по регионам (множественный выбор)
            if (filters.regions && Array.isArray(filters.regions) && filters.regions.length > 0) {
                if (!date.region || !Array.isArray(date.region)) return false;
                const hasRegion = date.region.some(r => filters.regions.includes(r));
                if (!hasRegion) return false;
            }
            
            // Фильтр по категориям (множественный выбор)
            if (filters.categories && Array.isArray(filters.categories) && filters.categories.length > 0) {
                if (!date.category || !Array.isArray(date.category)) return false;
                const hasCategory = date.category.some(c => filters.categories.includes(c));
                if (!hasCategory) return false;
            }
            
            // Фильтр по эре
            if (filters.era && filters.era !== 'all') {
                if (!date.era || date.era !== filters.era) return false;
            }
            
            // Фильтр по сложности
            if (filters.difficulty && filters.difficulty !== 'all') {
                if (!date.difficulty || date.difficulty !== filters.difficulty) return false;
            }
            
            return true;
        });
    },
    
    /**
     * Получение случайных дат для тренировки
     * @param {Array} datesArray - Массив дат
     * @param {number} count - Количество дат
     * @param {Object} filters - Фильтры для отбора
     * @returns {Array} - Массив случайных дат
     */
    getRandomDates: (datesArray, count, filters = {}) => {
        let pool = DateUtils.filterDates(datesArray, filters);
        
        // Перемешиваем
        const shuffled = [...pool].sort(() => Math.random() - 0.5);
        return shuffled.slice(0, count);
    },
    
    /**
     * Форматирование разницы между годами
     * @param {number} years - Количество лет
     * @returns {string} - Отформатированная строка
     */
    formatYearDifference: (years) => {
        const absYears = Math.abs(years);
        if (absYears >= 1000) {
            const centuries = Math.round(absYears / 100) / 10;
            return `${centuries} ${centuries === 1 ? 'тысячелетие' : 
                centuries > 1 && centuries < 5 ? 'тысячелетия' : 'тысячелетий'}`;
        }
        return `${absYears} ${absYears === 1 ? 'год' : 
            absYears > 1 && absYears < 5 ? 'года' : 'лет'}`;
    }
};
